
-- SELECT 문 실행 순서
---  FROM -> CONNECT -> WHERE -> GROUP BY -> HAVING -> ORDER BY

-- YEARWEEK별 LED_TV의 판매갯수  
SELECT YEARWEEK, -- YEARWEEK로 그룹바이 되어있기에 중복값은 일단 없다. 주차별 상품이 나오겠지.
       PRODUCT,
       SUM(QTY) AS RESULT
FROM KOPO_CHANNEL_RESULT
WHERE 1=1
AND PRODUCT IN ('LED_TV','HHP')
GROUP BY YEARWEEK,PRODUCT
ORDER BY RESULT DESC







SELECT * FROM KOPO_CHANNEL_RESULT



-- 뭐해볼까! ACCOUNTNAME 별 판매 QTY, QTY 0 인건 바꿔보자.


SELECT ACCOUNTNAME, --QTY가 0인 사람들 구하기. 그 중에 LED_TV를 판매대상으로 한 사람만 구하기.
       SALESID,
       QTY
FROM KOPO_CHANNEL_RESULT
WHERE QTY = 0
AND PRODUCT = 'LED_TV'
AND SALESNAME = 'AAFES'
GROUP BY ACCOUNTNAME,SALESID,QTY













